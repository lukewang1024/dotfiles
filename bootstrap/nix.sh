brew_install_pkgs()
{
  for pkg in "${pkgs[@]}"; do
    brew install "$pkg"
  done
  unset pkgs
}

install_nix_brew_runtimes()
{
  pkgs=(
    node
    python
    python@2
    ruby
  )
  brew_install_pkgs
}

